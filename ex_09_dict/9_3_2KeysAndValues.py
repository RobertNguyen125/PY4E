jjj = {'chuck': 1, 'fred': 42, 'jan': 100} # chuck is key while 1 is value
print(list(jjj)) #because jjj is dictionary so we can the result is [chuck, fred ,jan]
# which are the keys
print(jjj.keys()) #keys() is used to get the keys

print(jjj.values()) #values() is used to get values

print(jjj.items()) #items() is used to get both values and keys (tuples)
